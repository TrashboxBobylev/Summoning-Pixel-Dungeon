import com.watabou.noosa.audio.Sample;
import com.watabou.utils.Callback;
import com.watabou.utils.PathFinder;
import com.watabou.utils.PointF;
import com.watabou.utils.Random;

public class WandOfPrismaticLight extends DamageWand {

	{
		image = ItemSpriteSheet.WAND_PRISMATIC_LIGHT;

		collisionProperties = Ballistica.FRIENDLY_MAGIC;
	}

	@Override
	public int magicalmin(int lvl){
		return 1+Dungeon.hero.lvl/2;
	}

	@Override
	public int magicalmax(int lvl){
		return (int) (5+Dungeon.hero.lvl*1.2f);
	}

	@Override
	public float powerLevel(int level) {
		switch (level){
			case 0: return 1.0f;
			case 1: return 2.0f;
			case 2: return 0f;
		}
		return 0f;
	}

	@Override
	public float rechargeModifier(int level) {
		switch (level){
			case 0: return 1f;
			case 1: return 3f;
			case 2: return 1.33f;
		}
		return 0f;
	}

	@Override
	public void onZap(Ballistica beam) {
		affectMap(beam);
		
		if (Dungeon.level.viewDistance < 6 ){
			Buff.prolong( curUser, Light.class, 10f+buffedLvl()*5);
		}
		if ((!Dungeon.isChallenged(Conducts.Conduct.PACIFIST))) {
			Char ch = Actor.findChar(beam.collisionPos);
			if (ch != null) {
				processSoulMark(ch, chargesPerCast());
				affectTarget(ch);
			}
		}
	}

	public int blindLength(int level){
		switch (level){
			case 1: return 4;
			case 2: return 8;
		}
		return 2;
	}

    @Override
    public String statsDesc() {
        if (!levelKnown)
            return Messages.get(this, "stats_desc", magicalmin(0), magicalmax(0), blindLength(0));
        else
            return Messages.get(this, "stats_desc", magicalmin(), magicalmax(),  blindLength(level()));
    }

	private void affectTarget(Char ch){
		int dmg = damageRoll();

			Buff.prolong(ch, Blindness.class, blindLength(level()));
			ch.sprite.emitter().burst(Speck.factory(Speck.LIGHT), 6 );

		if (ch.properties().contains(Char.Property.DEMONIC) || ch.properties().contains(Char.Property.UNDEAD)){
			ch.sprite.emitter().start( ShadowParticle.UP, 0.05f, 10+buffedLvl() );
			Sample.INSTANCE.play(Assets.Sounds.BURNING);

			ch.damage(Math.round(dmg*1.333f), this);
		} else {
			ch.sprite.centerEmitter().burst( RainbowParticle.BURST, 10+buffedLvl() );

			ch.damage(dmg, this);
		}

	}

	private void affectMap(Ballistica beam){
		boolean noticed = false;
		for (int c : beam.subPath(0, beam.dist)){
			if (!Dungeon.level.insideMap(c)){
				continue;
			}
			for (int n : PathFinder.NEIGHBOURS9){
				int cell = c+n;

				if (Dungeon.level.discoverable[cell])
					Dungeon.level.mapped[cell] = true;

				int terr = Dungeon.level.map[cell];
				if ((Terrain.flags[terr] & Terrain.SECRET) != 0) {

					Dungeon.level.discover( cell );

					GameScene.discoverTile( cell, terr );
					ScrollOfMagicMapping.discover(cell);

					noticed = true;
				}
			}

			CellEmitter.center(c).burst( RainbowParticle.BURST, Random.IntRange( 1, 2 ) );
		}
		if (noticed)
			Sample.INSTANCE.play( Assets.Sounds.SECRET );

		GameScene.updateFog();
	}

	@Override
    public void fx(Ballistica beam, Callback callback) {
		curUser.sprite.parent.add(
				new Beam.LightRay(curUser.sprite.center(), DungeonTilemap.raisedTileCenterToWorld(beam.collisionPos)));
        Sample.INSTANCE.play( Assets.Sounds.RAY );
		callback.call();
	}

	@Override
	public void onHit(Wand wand, Char attacker, Char defender, int damage) {
		//cripples enemy
		Buff.prolong( defender, Cripple.class, 1f+ Dungeon.hero.lvl/3);
	}

	@Override
	public void staffFx(StaffParticle particle) {
		particle.color( Random.Int( 0x1000000 ) );
		particle.am = 0.5f;
		particle.setLifespan(1f);
		particle.speed.polar(Random.Float(PointF.PI2), 2f);
		particle.setSize( 1f, 2f);
		particle.radiateXY( 0.5f);
	}

}
